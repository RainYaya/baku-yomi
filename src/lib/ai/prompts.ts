export function buildAnalysisPrompt(
  original: string,
  chinese: string,
  userTranslation: string
): string {
  return `あなたは日本語教育の専門家です。学習者が中国語の翻訳文を見て日本語に回訳（バックトランスレーション）した結果を分析してください。

## 入力情報

**日本語原文:** ${original}
**中国語翻訳:** ${chinese}
**学習者の回訳:** ${userTranslation}

## 分析指示

学習者の回訳を原文と比較し、以下のフォーマットで分析結果を出力してください：

【総合評価】X/100

【誤り・差異分析】
以下のカテゴリに分けて各誤りを分析してください：
- 文法 / 語彙 / 助詞 / 敬語 / 時制 / 表現 / 語順 / その他

各誤りについて：
#### N. カテゴリ：簡潔な説明
- **回訳文:** （該当部分）
- **原文:** （該当部分）
- **分析:** 具体的な説明

【良い点】
学習者の回訳で特に良かった点を挙げてください。

【総合アドバイス】
今後の学習に向けたアドバイスを1-2文で述べてください。

## 注意事項
- 原文と完全一致でなくても、意味や文法が正しければ誤りとしないでください
- 自然な日本語表現の代替案がある場合は、それも提示してください
- 回答は日本語で行ってください`;
}
