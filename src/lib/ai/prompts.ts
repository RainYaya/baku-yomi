export function buildBacktranslateHintPrompt(
  original: string,
  chinese: string
): string {
  return `你是日语教育专家。学生在进行回译练习：看到中文翻译后，自己翻译成日语。

请提供**回译练习提示**，帮助学生从中文思考日语表达，而不是直接照抄原文语序。

## 输入
**日语原文:** ${original}
**中文翻译:** ${chinese}

## 输出要求

请从以下几个角度提供提示（选择2-4个最相关的）：

1. **语法结构**：指出原文中重要的语法点（如～際、～際に、～たところ等），但不要直接给出原文的完整表达
2. **语序与连接**：提示原文中从句/主句的连接方式，以及日语特有的语序特点
3. **时态与体态**：说明原文的时态（过去/现在/未来）和体态（完成/进行/反复）如何表达
4. **主语与对象**：提示动作主体（谁）和对象（谁被怎样）的表达方式
5. **动词活用**：提示重要动词的形态（可能形、受身形、使役形等）
6. **固定表达/惯用语**：提示文中的惯用表达或固定句型

## 格式
使用以下格式，每行一个提示：

【语法结构】提示内容...
【语序连接】提示内容...
【时态体态】提示内容...
【主语对象】提示内容...
【动词活用】提示内容...
【固定表达】提示内容...

## 注意事项
- 只提示方向和规律，不要给出原文的具体日语表达
- 帮助学生用中文思维去组织日语，而不是记忆原文
- 提示要简洁，2-3句话即可
- 回答使用中文`;
}

export function buildAnalysisPrompt(
  original: string,
  chinese: string,
  userTranslation: string
): string {
  return `你是日语教育专家。请分析学习者根据中文翻译回译成日语的结果。

## 输入信息

**日语原文:** ${original}
**中文翻译:** ${chinese}
**学习者回译:** ${userTranslation}

## 分析指示

请对比学习者的回译与原文，按以下格式输出分析结果：

【综合评分】X/100

【错误与差异分析】
请按以下分类分析每个错误：
- 语法 / 词汇 / 助词 / 敬语 / 时态 / 表达 / 语序 / 其他

每个错误的格式：
#### N. 分类：简洁说明
- **回译文:** （相关部分）
- **原文:** （相关部分）
- **分析:** 具体说明

【优点】
请列举学习者回译中做得好的地方。

【总体建议】
给出1-2句学习建议。

## 注意事项
- 即使与原文不完全一致，只要意思和语法正确，就不要标记为错误
- 如果有更自然的日语表达替代方案，也请提供
- 请使用日语回答`;
}
